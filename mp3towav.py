import os
import argparse
from pydub import AudioSegment

def mp3_to_wav(input_folder, output_folder):
    # Create the output folder if it does not exist
    if not os.path.exists(output_folder):
        os.makedirs(output_folder)

    # Convert each MP3 file to WAV and save it in the output folder
    for file_name in os.listdir(input_folder):
        if file_name.endswith('.mp3'):
            mp3_path = os.path.join(input_folder, file_name)
            wav_path = os.path.join(output_folder, os.path.splitext(file_name)[0] + '.wav')
            sound = AudioSegment.from_mp3(mp3_path)
            sound.export(wav_path, format='wav')

    print(f"Conversion complete. WAV files are saved in {output_folder}")

if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='Convert MP3 files to WAV format.')
    parser.add_argument('input_folder', help='Path to the folder containing the MP3 files.')
    parser.add_argument('output_folder', help='Path to the output folder where the WAV files will be saved.')
    args = parser.parse_args()
    
    mp3_to_wav(args.input_folder, args.output_folder)